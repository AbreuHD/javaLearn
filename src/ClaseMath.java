public class ClaseMath {
    public static void main(String[] args){

        System.out.println(Math.abs(8) + " & " + Math.abs(-8));
        System.out.println(Math.max(87,49));
        System.out.println(Math.ceil(3.8));
        System.out.println(Math.floor(3.8));
        System.out.println(Math.round(3.4));
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.exp(1));
        System.out.println(Math.log(10));
        System.out.println(Math.pow(10,3));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.toDegrees(1.57));
        System.out.println(Math.toRadians(90.00));
        System.out.println(Math.sin(90));
        System.out.println(Math.cos(90));

    }
}
